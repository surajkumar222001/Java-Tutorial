package Operators;

public class UnaryOperators {
    public static void main(String[] args) {
        // + - ++ -- !
             
        int num = +1;
        System.out.println(num);

        num = -num;
        System.out.println(num);

        num++;
        System.out.println(num);  

        num--;
        System.out.println(num);

        boolean IsAdmine = false;
        System.out.println(IsAdmine);
        System.out.println(!IsAdmine);

    }
}
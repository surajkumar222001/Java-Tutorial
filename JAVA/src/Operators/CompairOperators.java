package Operators;

public class CompairOperators {
    public static void main(String[] args) {
        // == , != , > , < , <= , >=
        int val1 = 1 , val2 = 2;
        if(val1 == val2) System.out.println("val1 is equal to val2");
        if(val1 != val2) System.out.println("val1 is NotEqual to val2");
        if(val1 > val2) System.out.println("val1 is greater to val2");
        if(val1 < val2) System.out.println("val1 is less to val2");
        if(val1 <= val2) System.out.println("val1 is less or equal  to val2");
        if(val1 >= val2) System.out.println("val1 is Greater or equal to val2");

    }
}

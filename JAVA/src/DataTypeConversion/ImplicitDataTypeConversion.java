package DataTypeConversion;

public class ImplicitDataTypeConversion {
    public static void main(String[] args){
      int a = 10;
      System.out.println("Int repersentation : " +a);
      
      long b = a;
      System.out.println("long repersentation : " +b);

      float c = b;
      System.out.println("float repersentation : " +c);

    }
}
